    private void setInformation() {
        BankCard bankCard = getInformation();
        System.out.println(bankCard.toString());
        if (bankCard == null) {
            JOptionPane.showMessageDialog(this, "Error! An error occurred. Please try again later.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        textNumberCard.setText(bankCard.getNumberCard());
        textNameCard.setText(bankCard.getNameCard());
        textNumberBalance.setText(String.format("%,d", bankCard.getNumberBalance()) + " VND");
        textCreateDate.setText(dateFormat.format(bankCard.getCreateDate()));
        textExpirationDate.setText(dateFormat.format(bankCard.getExpirationDate()));
        Image avatar = getImage(bankCard.getImageAvatar());
        imagePanel.setImage(avatar);
    }

    public BankCard getInformation() {
        return bankCardRepository.getInformationCard();
    }
    public Image getImage(byte[] data) {
        return new ImageIcon(data).getImage();
    }
